package com.DataTypes;


public class DataTypeExamples {

	public static void main(String[] args) {

		Byte bytevariable=30;
		bytevariable=127;
		System.out.println("The value of the bytevariable is:"+bytevariable);

		int intvariable=128;
		System.out.println("The value of intvariable is"+intvariable);

		short shortvariable=12789;
		System.out.println("The value of shortvariable is:"+shortvariable);

        long longvariable=1234567893;
		System.out.println("The value of longvariable is:"+longvariable);

		float floatvariable=123f;
		System.out.println("The value of floatvariable is:"+floatvariable);

		double doublevariable=2344.765;
		System.out.println("The value of double variable is:"+doublevariable);

		char charvariable='A';
		System.out.println("The value of charvariable is:"+charvariable);

		boolean booleanvariable=true;
		System.out.println("The value of booleanvariable is:"+booleanvariable);

		String  a="LiveTech";
		System.out.println("The value of a is"+a);
	}

}
